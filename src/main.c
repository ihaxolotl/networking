#include <stdio.h>
#include <stdlib.h>

#include "ip.h"

int main(void)
{
    int rc = -1;
    struct ipv4_header header;
    const unsigned char bytes[] = {
        0x45, 0x0,  0x0,  0x54, 0x85, 0x82, 0x40, 0x0,  0x40, 0x1,  0xb7, 0x24,
        0x7f, 0x0,  0x0,  0x1,  0x7f, 0x0,  0x0,  0x1,  0x8,  0x0,  0xd4, 0x6c,
        0x0,  0x5,  0x0,  0x1,  0x72, 0xf2, 0x29, 0x67, 0x0,  0x0,  0x0,  0x0,
        0xc6, 0x60, 0x2,  0x0,  0x0,  0x0,  0x0,  0x0,  0x10, 0x11, 0x12, 0x13,
        0x14, 0x15, 0x16, 0x17, 0x18, 0x19, 0x1a, 0x1b, 0x1c, 0x1d, 0x1e, 0x1f,
        0x20, 0x21, 0x22, 0x23, 0x24, 0x25, 0x26, 0x27, 0x28, 0x29, 0x2a, 0x2b,
        0x2c, 0x2d, 0x2e, 0x2f, 0x30, 0x31, 0x32, 0x33, 0x34, 0x35, 0x36, 0x37,
    };

    rc = ipv4_parse_header(bytes, sizeof(bytes), &header);
    if (rc < 0) {
        printf("Could not parse IPv4 header!\n");
        return EXIT_FAILURE;
    }

    ipv4_dump_header(&header);

    return EXIT_SUCCESS;
}
